package Objetos;

/**
 *
 * @author txaber
 */
public class Curso {
    
    private String codigo;

    public Curso(String codigo) {
        this.codigo = codigo;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    @Override
    public String toString() {
        return "Curso{" + "codigo=" + codigo + '}';
    }
    
    
}
